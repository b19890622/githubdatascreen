package com.CrossCountry.Survey.modelvo.donggs;

public class EquipmentNumber {
	private String equipmentName  ;                        //名字
	private  int  lengthwaysNum   ;                      //纵向数量
	private  int  quarantineNum   ;                      //隔离数量
	public String getEquipmentName() {
		return equipmentName;
	}
	public void setEquipmentName(String equipmentName) {
		this.equipmentName = equipmentName;
	}
	public int getLengthwaysNum() {
		return lengthwaysNum;
	}
	public void setLengthwaysNum(int lengthwaysNum) {
		this.lengthwaysNum = lengthwaysNum;
	}
	public int getQuarantineNum() {
		return quarantineNum;
	}
	public void setQuarantineNum(int quarantineNum) {
		this.quarantineNum = quarantineNum;
	}
	
	
}
