package com.CrossCountry.Survey.modelvo.dongl;

public class SecretTopTenEntity {
	String rank; // 排名
	String unit;// 单位
	String secretPassRate;// 密通率
	String onlineRate;// 在线率
	String breakNum;// 中断次数
	public String getRank() {
		return rank;
	}
	public void setRank(String rank) {
		this.rank = rank;
	}
	public String getUnit() {
		return unit;
	}
	public void setUnit(String unit) {
		this.unit = unit;
	}
	public String getSecretPassRate() {
		return secretPassRate;
	}
	public void setSecretPassRate(String secretPassRate) {
		this.secretPassRate = secretPassRate;
	}
	public String getOnlineRate() {
		return onlineRate;
	}
	public void setOnlineRate(String onlineRate) {
		this.onlineRate = onlineRate;
	}
	public String getBreakNum() {
		return breakNum;
	}

	public void setBreakNum(String breakNum) {
		this.breakNum = breakNum;
	}
	
	
}
