package com.CrossCountry.Survey.modelvo.dongl;

public class SafetyComplianceEntity {
	String ID;
	int checkDimen;
	int checkCount;

	public String getID() {
		return ID;
	}

	public void setID(String iD) {
		ID = iD;
	}

	public int getCheckDimen() {
		return checkDimen;
	}

	public void setCheckDimen(int checkDimen) {
		this.checkDimen = checkDimen;
	}

	public int getCheckCount() {
		return checkCount;
	}

	public void setCheckCount(int checkCount) {
		this.checkCount = checkCount;
	}

}
