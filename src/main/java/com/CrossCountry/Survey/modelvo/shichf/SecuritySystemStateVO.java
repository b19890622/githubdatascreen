package com.CrossCountry.Survey.modelvo.shichf;

public class SecuritySystemStateVO {
	private String name;// 设备名字
	private int value;// 数量
	private int flag;// 是否变红

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getValue() {
		return value;
	}

	public void setValue(int value) {
		this.value = value;
	}

	public int getFlag() {
		return flag;
	}

	public void setFlag(int flag) {
		this.flag = flag;
	}

}
